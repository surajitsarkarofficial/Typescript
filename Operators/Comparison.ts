// '===' for eqality check
// '!==' for not equal check
// '<' less than
// '>' greater than
// '<=' less than equal to
// '>=' greater than equal to

var num1:number = 100;
var num2:number = 200;
alert("Num1 is "+ num1);
alert("Num2 is "+num2);

// ===
alert("Is "+num1 +" === "+num2 +" - "+(num1===num2));

// !==
alert("Is "+num1 +" !== "+num2 +" - "+(num1!==num2));

// >
alert("Is "+num1 +" > "+num2 +" - "+(num1>num2));

// <
alert("Is "+num1 +" < "+num2 +" - "+(num1<num2));

// >=
alert("Is "+num1 +" >= "+num2 +" - "+(num1>=num2));

// <=
alert("Is "+num1 +" <= "+num2 +" - "+(num1<=num2));