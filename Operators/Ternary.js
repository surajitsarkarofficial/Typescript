// expression ? value1 : value 1
// If expression is true then value1
// If expression is false then value 2
var num1 = 100;
var num2 = 200;
console.log((num1 < num2) ? (num1 + " is less than " + num2) : (num2 + " is less than " + num1));
console.log((num1 === num2) ? (num1 + " is equal to " + num2) : (num1 + " is not equal to " + num2));
